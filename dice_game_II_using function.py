from random import randrange
def roll_dice():
    dice_sides = [
        " \n 0 \n ",
        "0 \n \n 0",
        " 0\n 0 \n0 ",
        "0 0\n \n0 0",
        "0 0\n0\n0 0",
        "0 0\n0 0\n0 0"
        ]
    rolled = randrange(6)
    print(dice_sides[rolled])
while input("press q to run: ") == 'q':
    roll_dice()
